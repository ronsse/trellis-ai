def test_advisory_wiring_attaches_advisories_to_packs(
    sqlite_registry: StoreRegistry,
) -> None:
    """Verify the §5.5.1 row 2 wiring fix: advisories generated by the
    first periodic pass actually get attached to subsequent packs and
    accumulate presentations the fitness loop can score.

    Smoke-tests the chain:
      AdvisoryGenerator.put_many → AdvisoryStore → PackBuilder
        → PACK_ASSEMBLED.advisory_ids → run_advisory_fitness_loop
    """
    from trellis.stores.base.event_log import EventType

    report = run(
        sqlite_registry, seed=0, rounds=15, feedback_batch_size=5, traces_per_domain=4
    )
    assert report.metrics["loops.advisory_runs"] >= 2.0
    assert report.metrics["loops.advisories_generated_total"] >= 1.0

    # If the wiring is correct, at least one PACK_ASSEMBLED event after
    # the first periodic pass carries a non-empty ``advisory_ids``.
    pack_events = sqlite_registry.operational.event_log.get_events(
        event_type=EventType.PACK_ASSEMBLED, limit=1000
    )
    advisory_attachments = sum(
        1 for ev in pack_events if ev.payload.get("advisory_ids")
    )
    assert advisory_attachments > 0, (
        "no PACK_ASSEMBLED event carried advisory_ids — advisory_store is "
        "not wired into PackBuilder, or generation never produced any "
        "advisory above the confidence threshold"
    )


def test_advisories_generated_only_once_across_periodic_passes(
    sqlite_registry: StoreRegistry,
) -> None:
    """``AdvisoryGenerator.generate`` mints fresh ULIDs per call; if the
    scenario regenerated each batch, every fitness pass would face a
    new cohort of zero-presentation advisories. Verify generation
    happens exactly once across the full run regardless of how many
    periodic passes fire."""
    report = run(
        sqlite_registry, seed=0, rounds=15, feedback_batch_size=5, traces_per_domain=4
    )
    # 15 rounds with batch_size=5 → 3 periodic passes. Generation
    # should still happen on only the first one.
    assert report.metrics["loops.advisory_runs"] == 3.0
    # generated_total counts the union over all passes; with single-pass
    # generation this equals what was emitted on pass 1 — typically a
    # small number on this synthetic corpus.
    assert 0 <= report.metrics["loops.advisories_generated_total"] <= 30


class TestRegimeShiftMode:
    """Opt-in regime-shift mode demonstrates the advisory suppression
    branch end-to-end in scenario context (§5.5.2 row 1).

    Pre-shift the agent grades against the canonical
    ``required_coverage``; post-shift the first N entries are swapped
    for unreachable placeholders so coverage drops and packs start
    failing. Combined with ``advisory_min_sample_size=2``, per-entity
    advisories form on batch 1 and at least some get suppressed once
    post-shift evidence accumulates.
    """

    def test_regime_shift_off_preserves_default_baseline(
        self, sqlite_registry: StoreRegistry
    ) -> None:
        """Default kwargs (``regime_shift_round=None``) — gate on the
        primary convergence signal (``useful_delta``) per the §5.5.1
        row 3 fix. ``weighted_delta`` is informational because the
        domain_context profile's heavy breadth weight can drive it
        negative as the noise loop correctly trims non-referenced
        items."""
        report = run(
            sqlite_registry,
            seed=0,
            rounds=30,
            feedback_batch_size=5,
        )
        assert report.metrics["loops.advisories_suppressed_total"] == 0.0
        assert report.metrics["convergence.useful_delta"] >= -0.05
        # All three domains reach success_rate 1.0 post-corpus-fix.
        for domain in ("software_engineering", "data_pipeline", "customer_support"):
            assert report.metrics[f"per_domain.{domain}.success_rate"] >= 0.8, (
                f"{domain} success_rate dropped below 0.8"
            )

    def test_regime_shift_disrupts_useful_fraction(
        self, sqlite_registry: StoreRegistry
    ) -> None:
        """With regime shift on, the agent's useful_fraction drops
        post-shift because the unreachable replacements aren't in any
        pack. The fitness loop runs and updates confidence on whatever
        advisories exist (boost/down-blend). On the post-§5.5.1-row-3
        corpus the per-domain rates are uniform pre-shift so
        anti-pattern advisories don't naturally form — suppression
        does *not* fire end-to-end here. The suppression branch is
        unit-tested in
        ``test_effectiveness.py::test_suppresses_failing_advisory``.
        """
        report = run(
            sqlite_registry,
            seed=0,
            rounds=30,
            feedback_batch_size=5,
            regime_shift_round=15,
            advisory_min_sample_size=2,
            # Convergence delta is allowed to swing under regime shift —
            # this scenario is about exercising disruption + advisory
            # confidence updates, not about passing the regress gate.
            convergence_delta_regress_threshold=-1.0,
        )
        # The loop does run and update confidence: at least one
        # advisory's confidence moved (boost or down-blend).
        assert report.metrics["loops.advisory_runs"] >= 1.0
        assert report.metrics["loops.advisories_generated_total"] >= 1.0
        # Post-shift packs fail — round_success_rate must drop below
        # default 1.0.
        assert report.metrics["round_success_rate"] < 1.0

    def test_regime_shift_round_must_be_non_negative(
        self, sqlite_registry: StoreRegistry
    ) -> None:
        with pytest.raises(ValueError, match="regime_shift_round must be non-negative"):
            run(sqlite_registry, regime_shift_round=-1)

    def test_effective_required_coverage_replaces_post_shift(self) -> None:
        """Unit-level check on the post-shift swap so future refactors
        can't silently change which entities the agent grades against."""
        from eval.generators.trace_generator import EvalQuery
        from eval.scenarios.agent_loop_convergence.scenario import (
            _effective_required_coverage,
        )

        query = EvalQuery(
            domain="x",
            intent="test",
            required_coverage=["a", "b", "c"],
        )

        pre_shift = _effective_required_coverage(
            query,
            round_index=10,
            regime_shift_round=15,
            regime_shift_replacement_count=2,
        )
        assert pre_shift == ["a", "b", "c"]

        post_shift = _effective_required_coverage(
            query,
            round_index=15,
            regime_shift_round=15,
            regime_shift_replacement_count=2,
        )
        # First two replaced with placeholders; the third stays.
        assert post_shift[0].startswith("_unreachable_post_shift_")
        assert post_shift[1].startswith("_unreachable_post_shift_")
        assert post_shift[2] == "c"


def test_distractors_dont_overlap_with_required_coverage() -> None:
    """Distractor doc ids must not collide with any ``doc:<entity>`` id.

    Otherwise the scenario can't tell distractors from real entities
    when grading rounds.
    """
    from eval.generators.trace_generator import DOMAIN_TEMPLATES

    real_doc_ids = {f"doc:{entity}" for t in DOMAIN_TEMPLATES for entity in t.entities}
    distractor_ids = {
        doc_id for docs in _DISTRACTOR_DOCS.values() for doc_id, _ in docs
    }
    assert real_doc_ids.isdisjoint(distractor_ids)
